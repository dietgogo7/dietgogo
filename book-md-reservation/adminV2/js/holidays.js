/**
 * 날짜 문자열(YYYY-MM-DD)이 공휴일인지 확인합니다.
 * @param {string} dateStr - 'YYYY-MM-DD' 형식의 날짜 문자열
 * @returns {string|null} 공휴일 명칭 (공휴일이 아니면 null)
 */
function getHolidayName(dateStr) {
    if (HOLIDAYS_DB[dateStr]) return HOLIDAYS_DB[dateStr];
    return null;
}

/**
 * 해당 날짜 객체가 공휴일인지 확인합니다.
 * @param {Date} dateObj - Date 객체
 * @returns {boolean} 공휴일 여부
 */
function isHoliday(dateObj) {
    const yyyy = dateObj.getFullYear();
    const mm = String(dateObj.getMonth() + 1).padStart(2, '0');
    const dd = String(dateObj.getDate()).padStart(2, '0');
    const dateStr = `${yyyy}-${mm}-${dd}`;
    return !!HOLIDAYS_DB[dateStr];
}

